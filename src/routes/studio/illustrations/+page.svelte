        <p>
            As a kid, I used to draw a lot. Not anything you might call good
            art--I wasn't drawing with intention to get better, but because I
            really liked putting things in my head on paper in some way.
        </p>
        <p>
            Even as a writer, I've always been quick to eschew words when a
            picture will do. In high school, I fell into the world of graphic
            novels and dreamed of making something similar. But there was
            already that nagging sense of, "well--I'm not that good and I won't
            ever be." I bounced off art class in school and never really looked
            back. I thought it was just a natural skill I didn't have.
        </p>
        <p>
            In 2018 or so, I got into Photoshop in a big way, spending several
            hundred hours editing other people's photos, trying my hand at
            everything from beautifying to fully replacing one face with
            another. Always I was drawn to portraiture. I didn't think of it as
            art as much as tech.
        </p>
        <p>
            But when I got my first drawing tablet in 2020, I realized how
            similar the two can be. That digital art can be a series of
            manipulations just as much as anything else. All my paintings are
            done from scratch, yes, but I use a lot of unconventional ways to
            get to a final result. I'm always trying to find interesting
            shortcuts that give the painting a style that I like.
        </p>
        <p>
            At the end of the day, though, I've fallen in love with the
            brushtrokes themselves. The process of slowly, arduously, bringing a
            spark of life into existence. Painting is how I learned that we're
            never too old to pick up a new skill. That natural gifting is a bit
            of a misleading path. You can work at anything.
        </p>
        <p>
            My paintings don't have much rhyme or reason. They're fairly
            unorganized as I just try to keep improving my skills, relaxing,
            painting what looks cool to me in the moment. Maybe one day I'll get
            to that graphic novel. But for now, it's just one portrait at a time.
        </p>
    </div>
