<h2 id="1">Why is there a new site?</h2>

<p>
    I started this site in Summer 2022, after hitting the creative limitations
    of a heavily Frankensteined Squarespace template. It had custom components
    and styling just glued willy nilly and was looking <em>way</em> too similar to
