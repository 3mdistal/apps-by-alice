<h2 id="2">Nerding out about design choices</h2>

<p>I want a site that makes people smile.</p>

<p>
    I love a clean-looking application, but boy am I tired of Apple derivative.
    I think I can count on one hand the fonts I encounter in a day.
</p>

<p>
    And I'm not saying that's bad, but I definitely want something that lets
    folks take a break from the rest of the Internet, even if some of those
    risky designs don't always work out at first.
</p>

<p>
    The biggest departure from the norm was combining my homepage and menu into
    one. When you land on the site you have five options. This is risky from a
    UX perspective: will users bounce off the site because of choice paralysis?
    But I also come at it from a games philosophy background: when you offer
    someone a simple choice, they become more invested in the journey to follow.
</p>

<p>
    The real trick, then, is to make sure the choice feels as clear as possible:
</p>

<ul>
    <li>Curious what the site is? About.</li>
    <li>Came here to work with me? Commissions.</li>
    <li>Want to read the blog? .¬†.¬†.¬†Blog.</li>
    <li>Just wanna chat? Connect</li>
</ul>

<p>
    ‚ÄúStudio‚Äù is the option I struggled with. I knew I wanted my own creative
    work to have a dedicated section, but since my work doesn't fit into one
    medium or genre, I needed a word to encapsulate it. I still think it's more
    confusing than the others, so if you have any suggestions, I'm open.
</p>

<p>
    The rest of the design, then, needs to tell the user that the choice is
    super low stakes:
</p>

<ul>
    <li>
        <strong>Pastels</strong>, to me, scream <em>relax</em>. Er, say
        <em>relax</em>
        soothingly. While I have a dark mode planned, and I'm definitely still developing
        the site's visual identity, I feel like the homepage and tops of each landing
        page give me the palette I'd like to smear over the rest of the site.
    </li>
    <li>
        <strong>Shape language</strong> is another aspect of the site that I want
        to push. It's clear on the homepage but quickly lost elsewhere. This will
        just take time and technical skill. Working with the shapes on the homepage
        wasn't an easy programming task (as anyone who's worked with `clip-path`
        might know). I need to find more efficient ways to translate weird shapes
        to all screen sizes.
    </li>
    <li>
        <strong>Animations</strong> <em>really</em> tell the user it's okay to have
        fun. However, animations can just as easily confuse when done poorly. It's
        a constant balancing act of fun vs. annoying, and I've been on too many sites
        whose overwrought animations make me disengage with content. I'm definitely
        still finding what works, and as the person who uses my site the most, I've
        edited animations to be faster far more frequently than slower.
    </li>
</ul>

