<h2 id="3">What to expect from the blog</h2>

<p>
    I've been even quieter than usual the past two years on socials, besides the
    occasional illustration. While there's a lot of personal reasons for that I
    hope to write about later, I can safely say now that I'm going to be
    breaking that silence.
</p>

<p>
    For me, the blog is a space for less edited content than my studio work,
    which tends to ferment for literal years. It's a place for me to be me in a
    very unpolished way, which enables me to engage with my communities without
    so much pretense.
</p>

<p>
    And I have a lot of communities. I'm passionate about the worlds of web
    technology, gaming, literary fiction and fantasy, music productions, digital
    art, poetry, movies, and so much more. Despite all the best advice telling
    me to pick a niche, I don't feel the need.
</p>

<p>
    I will, however, develop a categorization system for the blog, so you can
    easily find content that interests you and skip the rest. That also will
    help me to write specifically to each audience.
</p>

<p>
    Finally, I'll be using the blog to keep you posted about studio releases,
    website updates, and other life changes. I've also played with the idea of a
    newsletter, but maybe that's too 2010s. Let me know what you prefer!
</p>

<h2 id="4">Waxing philosophical about creating in public</h2>

<p>
    For those of you who were readers of my old site, you may be asking, ‚ÄúWhy
    did you take down all your writing?‚Äù
</p>

<p>To which I honestly answer, I haven't coded that part of the site yet.</p>

<p>
    But it's also given me a chance to think, deeply, about what I've created
    and where I want to focus my efforts moving forward. About how I want to
    draw attention to the content I've worked so hard on.
</p>

<p>
    While more formal publication has been (and will still be) in the cards for
    much of my work, I'm also motivated to create my own distribution method to
    best serve the content.
</p>

<p>
    What do I mean by this? My poems often have long lines or tricky formatting.
    I've sometimes photoshopped stories to creatively destroy the text. While
    journals have graciously accepted these experimental pieces, I have seen a
    lot of compromises made to fit the works in print or on the web.
</p>

<p>
    Also, I don't want to share work when it feels <em>stale</em> to me. Writers
    often wait years before seeing work accepted, and longer before it's actually
    published. Often many years. By the time the readers engages, the author has
    moved on.
</p>

<p>
    I want to engage with you on work that's coming from where I am now. Yeah,
    that's way more vulnerable, but it also allows me to learn and better my
    art.
</p>

<p>
    I'm planning to take this a step further in my studio, too: I'm going to
    publish both finished and unfinished work here, gaining valuable feedback
    from my very digital community. I'll mark clearly which is which, so you can
    decide how you wish to engage.
</p>

