<h2>Final thoughts</h2>

<p>
    More than anything, I want to convey how much of this‚Äîthe site, the content,
    the very act of creation‚Äîis a process with which I'm going to be more
    transparent. I'm so tired of writing things and not sharing. Of learning and
    not teaching. My professional life has taught me how to work more in public,
